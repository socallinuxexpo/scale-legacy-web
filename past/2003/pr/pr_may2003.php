The USC, Simi/Conejo, and UCLA Linux User Groups are proud to present the second annual Southern California Linux Expo scheduled for November 22nd, 2003.<br><br>

<b>Call For Papers</b><br><br>

The USC, Simi/Conejo, and UCLA Linux User Groups are proud to present the second annual Southern California Linux Expo scheduled for November 22nd, 2003 at the Los Angeles Convention Center. The Southern California Linux Expo will bring together Linux and Open Source Software companies, developers, and users.<br><br>

The conference is being held by Linux user groups from the local area: the USCLUG, SCLUG, and UCLALUG. Building on the tremendous success of last year's SCALE, the three LUGs will expand on previous LUG Fests to promote Linux and the Open Source Software community.<br><br>

We INVITE you to share your work on Linux and Open Source projects with the rest of the community.<br><br>

Presentations can be in the form of speeches, tutorials, or demonstrations. A speech on Linux's uses in multimedia, a tutorial on iptables (demonstrating routing and firewall applications in Linux), or a demonstration of projects such as games, are just some examples of appropriate material.<br><br>

<b>Presentation Guidelines</b><br><br>

Presentations are allotted time in increments of 30 minutes up to 2 hours. For speeches, please submit your paper along with a description of the speech format and intended audience. For tutorials and demonstrations, please submit your proposal and let us know of any special needs such as audio/video equipment.<br><br>

<b>Timeline</b><br><br>

The deadline for submissions is Sunday, October 6th at midnight. We will review your submissions and contact you shortly.<br><br>

<b>How To Submit</b><br><br>

Please send your paper proposal, perferably in HTML or ASCII format, to <a HREF="mailto:kapadia=at=socallinuxexpo.com" CLASS="light">kapadia=at=socallinuxexpo.com</a>.<br><br>

