	Born the son of a pig farmer in Bucharest, this young ne'er-do-well had
	few ambitions above mucking out the slop stall before dinner.<br><br>
	
	But that was just at the dawn of the digital age.  Who would have
	thought that five years later the same boy who thought cow tipping
	shouldn't go above 10 percent would go on to invent the Internet, and
	eventually become the first living human with an ADSL line surgically
	attached to his spinal column.<br><br>
	
	Now, in these increasingly untethered times, he has eschewed his former
	6Mbit neural I/O port for an 11Mbit, encrypted, wireless version.  It
	certainly makes it easier to leave the house without the need for miles
	of extension cord.<br><br>
	
	Rob is the author of three O'Reilly books, <a href="http://www.oreilly.com/catalog/wirelesscommnet2/">Building Wireless Community Networks, 2nd Edition</a>, <a href="http://www.oreilly.com/catalog/linuxsvrhack/">Linux Server Hacks</a>, and <a href="http://www.oreilly.com/catalog/wirelesshks/">Wireless Hacks</a>.  He recently served as sysadmin for <a href="http://www.oreillynet.com/">the O'Reilly Network</a>, and is currently working on promoting community wireless networking through efforts like <a href="http://freenetworks.org/">FreeNetworks.org</a> and <a href="http://nocat.net/">NoCat.net</a>.<br><br>

