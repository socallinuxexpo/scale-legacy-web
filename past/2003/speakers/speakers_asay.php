	<i><b>Matt's talk is titled "Making Money on Free: Revenue Models for Open
	Source Businesses"</b></i><br><br>

	Abstract:  It appears that the day of 80% margins for software are over.
	Though IBM still publicly claims 83% software margins, open source is
	increasingly cutting into margins for IBM, and every other software
	company.  Indeed, open source is challenging the very notion of
	intellectual property, the lever used by ISVs to collect "rents" on their
	innovations.<br><br>

	This presentation details new models for making money on free and open
	source software.  It also suggests that the best models are those that
	acknowledge and respect the seachange that open source has forced upon the
	industry, rather than fight against it.  It will detail the experiences of
	several companies (hardware and software, at both the startup and Fortune
	500 levels) in moving to these new models, and suggest ways for existing
	companies to make the transition from closed source to open source.

