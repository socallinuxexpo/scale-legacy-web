	The SCLUG held several LUG Fests, gatherings of Los Angeles Linux user
	groups, in the previous years. The SC LUG contributes its extensive
	experiences and expertise to help organize the Southern California Linux
	Expo. For more information on the SC LUG, go to
	<A HREF="http://www.sclug.org/">http://www.sclug.org/</A>
	</p>

	<p CLASS="bgwhite">
