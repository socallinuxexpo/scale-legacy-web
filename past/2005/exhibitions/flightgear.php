	<br /><br />
	FlightGear is a free flight simulator project. It is being developed through the gracious contributions of source code and spare time by many talented people from around the globe. Among the many goals of this project are the quest to minimize short cuts and "do things right", the quest to learn and advance knowledge, and the quest to have better toys to play with.<br /><br />

	The idea for Flight Gear was born out of a dissatisfaction with current commercial PC flight simulators. A big problem with these simulators is their proprietariness and lack of extensibility. There are so many people across the world with great ideas for enhancing the currently available simulators who have the ability to write code, and who have a desire to learn and contribute. Many people involved in education and research could use a spiffy flight simulator frame work on which to build their own projects; however, commercial simulators do not lend themselves to modification and enhancement. The Flight Gear project is striving to fill these gaps.<br /><br />

FlightGear will be used to drive a full scale 747-400 simulator cockpit to
demonstrate the robustness and extendibility of the FlightGear architecture
as well as the suitability of Linux to support large, distributed processing
applications. The goal of the simulator project is to develop accurate
models of the 747-400  systems and a basic software and hardware
architecture that can be used to create a variety of simulator cockpits
representing additional aircraft types and models.<br /><br />

	For more information on FlightGear see: <a href="http://www.flightgear.org">www.flightgear.org</a>. Additional information on the 747 Simulator Project can be found on the FlightGear website under the <a href="http://www.flightgear.org/Projects/">www.flightgear.org/Projects</a> page.
