	<h3>Open Source Multimedia</h3>

	<p>
	Where's the video? The open source community has made great strides in
	operating systems, databases, web servers, and desktop productivity, to
	name a few, but is still in desperate need of first-class audio and
	video experiences. Without world-class A/V applications, proprietary
	environments will continue to develop and prosper. Looking at current
	trends and future predictions, this keynote address will discuss the
	state of open source multimedia today and what needs to be done to
	ensure that the future of open source development remains bright.
	</p>
