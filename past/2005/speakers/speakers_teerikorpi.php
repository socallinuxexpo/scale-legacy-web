	<h3>When to Application Cluster</h3>

	<p>
	This talk will explore several key questions to answer before
	determining whether an application cluster is the right solution for
	your business requirements. Further, key criteria will be presented
	that will help you be able to evaluate the right solution for your
	needs.<br /><br />

	First, an overview of Linux application clustering will be presented.
	From this, we will cover the criteria you can use to decide when an
	application cluster is the right solution. Second, we will consider
	different organizational and application requirements in order to
	determine how you might investigate which solution type is best for
	your needs. Then, we will discuss how to develop key requirements based
	on this needs analysis in order to provide assessments or evaluation
	criteria. Finally, we will propose some suggestions for solutions to
	Linux application clustering.
	</p>

