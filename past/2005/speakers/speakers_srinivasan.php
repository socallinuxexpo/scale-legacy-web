	<h3>
	Globalization of grassroots technologies in India: A political-economy study of a FLOSS in developing countries
	</h3>

	<p>
	This talk will be based on Srinivasan's dissertation research which
	studies grassroots technologies on a global scale and in the context of
	developing countries to understand how the digital divide is being
	transformed. Political-economy players are acknowledging and adopting
	the power of such technologies in third and first worlds. Open-source
	technologies are gaining importance so much that corporate and
	governmental actors are endorsing them, as evident in a case study of
	Linux in India. The government of India recently announced an
	Open-Source Initiative as encouraging the adoption of open-source
	software in its various applications.<br /><br />
	
	This research specifically focuses on Linux and the political-economy
	of its globalization in an Indian city. Indian Linux User Groups or
	ilugs are a communities of Linux users and activists with chapters in
	cities all over India. Still in a nascent stage of development, ilugs
	communicate via list-servs and monthly meetings to discuss technical
	and social issues of using Linux. Significant evidences of Linux
	adoption over the past three years include the first Indian Linux
	magazine and Linux-Asia conferences hosted in India.
	</p>

