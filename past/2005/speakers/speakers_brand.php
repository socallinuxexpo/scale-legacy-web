	<h3>Open Source Solutions for Small Schools</h3>

	<p>
	Schools, and in particular small private and parochial schools, represent a real opportunity for open source software.  Due to budget constraints, these schools are more receptive to non-mainstream software that costs less to acquire and maintain.  These schools typically depend on volunteers.  An offer of help from a GNU/Linux advocate - or even a LUG - can be an important and valuable contribution that will be much appreciated.  A successful installation can be a great source of credibility when promoting Open Source for commercial applications.<br /><br />

	This presentation will summarize the value proposition for GNU/Linux and other Open Source Software to small schools.  If you are a potential volunteer, it will give you some advice about what you can do to leverage your time into the most benefits for the school you want to help.  If you are a consultant or IT services company, it will describe our practices and current projects and other resources that can help you successfully develop this market.
	</p>

