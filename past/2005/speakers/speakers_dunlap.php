	<h3>Randy Dunlap</h3>

	<p>
	Holding a Masters degree in Information and Computer Science from
	Georgia Tech, Randy Dunlap has spent over 25 years in the field of
	software development and hardware/software co-design and
	co-development. He has worked with an array of systems and networks
	including multi-user minicomputers (operating systems, firmware, and
	communications software development), NetWare network drivers and SCSI
	drivers. Prior to joining OSDL, Dunlap was the Linux USB subsystem
	maintainer while at Intel.
	</p>

	<h3>The Linux Kernel Janitors Project: Getting Involved in Linux
	Development</h3>

	<p>
	This talk begins with a description of the Linux kernel
	janitors project and how it works, then moves on to how
	anyone can become involved in kernel patches and development.
	It discusses working via mailing lists, etiquette, documentation,
	patch formats, working with maintainers, and some examples,
	then proceeds to related projects and tools and how to use
	them.  It wraps us with a summary of the OSDL and its Linux
	projects.
	</p>
