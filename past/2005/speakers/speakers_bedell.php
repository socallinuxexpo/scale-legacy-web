	<h3>Kevin Bedell</h3>

	<p>
	Kevin Bedell is Director of Consulting and Training for Black Duck
	Software, a company providing services and technologies to help their
	customers safely accelerate their use of open source software. In this
	position, Kevin works with Black Duck's customers to help them manage
	software compliance and licensing issues and develop strategies for managing
	open source in their software development organizations. Kevin also works
	with companies interested in scanning their code bases to understand what
	open source software and associated licensing obligations exist there.<br /><br />

	Kevin has been active in the open source community including writing a book
	on open source Java technologies. He was founding editor of LinuxWorld          Magazine and continues with the magazine as Open Source Editor. He serves as    the Community Manager for linux.java.net and is on the Java.net management      board. Kevin keeps an infrequent but popular weblog on the O'Reilly network     and has written software on and off for over 20 years.<br /><br />

	Kevin earned a BS in Engineering from Michigan Tech and an MBA from the
	Crummer Graduate School of Business at Rollins College.
	</p>

	<h3>
	How Open Source is Transforming Corporate Software Development
	</h3>

	<p>
	Open source software and programming libraries have dramatically changed
	the way software is developed. Leveraging open source as a part of the
	software development process drives down both development time and cost
	while increasing software quality. The defining question for most companies
	is no longer, 'Should we use open source?', but rather, 'How can we
	accelerate our adoption of open source while doing so safely?'<br /><br />

	This session presents an overall view of the entire software development
	value chain from programmer to customer -- including in-house developers,
	off-shore developers, open source contributors and others along the way --
	and identifies best practices for each stop that will allow companies to
	accelerate their ability to utilize open source while making sure they are
	managing the process effectively.<br /><br />
	This session will explore best practices in the areas of:
	</p>

	<ul>
	<li>Aligning open source usage with corporate culture and business goals</li>
	<li>Processes and policies around the software development process</li>
	<li>Acquiring code developed by outsourced or off-shore partners</li>
	<li>Understanding license obligations in the finished product or project</li>
	<li>How to plan and manage the overall process effectively</li>
	</ul>

