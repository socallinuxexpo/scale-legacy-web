	<h3>Open Development for Solaris and Linux</h3>

	<p>
	When one speaks of open source operating systems, you immediately think
	of Linux. However, this is likely to change in 2005 as Sun releases an
	open source version of Solaris. Already, hundreds of users are
	accessing the beta version of opensolaris.org and Solaris 10 will have          the ability to run Linux binaries natively on the x86 platform with
	Project Janus. However, this isn't a story about Solaris vs Linux, it
