	<h3>About The Southern California Linux Expo 2005</h3>

	<p>
	The USC, Simi/Conejo, and UCLA Linux User Groups are proud to present
	SCALE 3X: The Third Annual Southern California Linux Expo. To be held
	in February 2005, the Southern California Linux Expo will bring together
	Linux and Open Source Software companies, developers, and users.
	</p>

	<p>
	This event will provide new insight in the growing
	technological Linux community and where it is headed.  This operating
	system created by a grass roots effort has grown robust enough to be
	embraced by companies such as Sun and IBM. The Southern California Linux
	Expo will contain a blend of business leaders and technical experts that
	are required to truly understand where Linux is at and where it will be
	in 10 years.
	</p>

	<h3>About The Southern California Linux Expo, Inc</h3>
	<p>
	The Southern California Linux Expo, Inc is a non-profit organization
	currently comprised, but not limited to, members of the USCLUG,
	UCLALUG, and SCLUG. Our mission statement is as follows:
	</p>

	<p>
	The mission of SCALE is outreach - our goal is to evangelize the
	advantages of Open Source software. Open Source software is any software that meets the litmus test of the OSI (Open Source Initiative)<sup><a href="http://www.opensource.org/docs/definition.php">1</a></sup>. Examples of OSS are Linux and the various BSD operating systems, and applications such as OpenOffice.org and Mozilla.
	</p>

	<p>
	Our target audiences are both current and potential users of OSS<sup><span title="Yes, we understand that includes every person on the planet..." style="cursor:help;border-bottom:dashed 1px #999;">2</span></sup>. Those users can be individual computer users, educational institutions, or businesses.
	</p>

	<p>
	Those outreach efforts consist mainly of, but are not limited to, the Southern California Linux Expo. The Expo's educational focus is composed of (but not limited to) technical seminars and booths where, among other things, both commercial software and hardware vendors, and local non-profit groups (for example, Linux Users Groups), participate in product display, and software demonstrations and tutorials, respectively.
	</p>

	<p>
	SCALE may host, or participate in, other activities that align with its mission statement.
	</p>

	<p>
	We are a non-profit volunteer organization, and are vendor- and distribution neutral in our outreach efforts.
	</p>

	<h3>About The LUGs</h3>

	<p>
	The University of Southern California's (USC) Linux Users Group is a
	community of users of various unices such as Linux, *BSD, Solaris, etc.
	Working closely with the USC Computer Science Department, the USC LUG
	provides much of the resources necessary to put together the Southern
	California Linux Expo. Visit the USC LUG website at
	<a href="http://linux.usc.edu/">http://linux.usc.edu/</a>
	</p>

	<p>
	The Simi/Conejo Linux Users Group (SC LUG) is a group of Linux enthusiasts
	from the Simi Valley / Conejo Valley area just north of Los Angeles.
	The SCLUG held several LUG Fests, gatherings of Los Angeles Linux user
	groups, in the previous years. The SC LUG contributes its extensive
	experiences and expertise to help organize the Southern California Linux
	Expo. For more information on the SC LUG, go to
	<a href="http://www.sclug.org/">http://www.sclug.org/</a>
	</p>

	<p>
	The University of California, Los Angeles (UCLA) Linux Users Group has been
	in existence since 1998. The UCLALUG promotes Linux via advocacy, support,
	and education to UCLA and the surrounding areas. Seeking to help out, the
	UCLALUG contributes its resources to the Southern California Linux Expo.
	Check out the UCLA LUG and its mirror of Free Software at
	<a href="http://linux.ucla.edu/">http://linux.ucla.edu/</a>
	</p>

