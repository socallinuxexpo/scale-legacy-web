	<i>Darren Moffat's speech will be on the topic of Tuning Unices For
	Security</i><br><br>

	An all powerful single user account is often too much power in one persons
	hands to perform the jobs they need to do. A form of RBAC has been
	available in versions of Solaris for over 10 years but only became main
	stream in Solaris 8.  Many other systems (including most mainstream
	GNU/Linux distributions use sudo to achieve a similar goal of giving out
	only the privleges needed to admin users.<br><br>

	Darrent Moffat will give an overview of some of the areas of compatibility
	and divergence in some OS security features between Solaris and Linux
	(RedHat will be the baseline comparison). The main focus will be on RBAC
	(Role Based Access Control), PAM and Kerberos.<br><br>

	Darren will also discuss some of the technical difficulties in manging open
	source based components in Solaris (this is NOT a GPL/BSD license rant but
	is about technical issues).<br><br>

