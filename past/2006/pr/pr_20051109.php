</p>

<p>
If you're interested in Linux and Open source Software, the Southern California Linux Expo is for you. SCALE has seminars by well-known speakers, tutorials for beginners, and both vendor booths and non-commercial booths.
</p>

<p>
These stellar speakers have been added to the program for SCALE4x:<br /><br />

<ul>
<li>Matt Asay, VP of Business Development, Alfresco - "Open Source: The Highest Stage of Technology Capitalism or, The Next Decade of Software"</li>

<li>David Schachter, Sleepycat Software Storage - "Management in the LAMP Stack"</li>

<li>Janinne Brunyee, Senior Director, Solutions Marketing RealNetworks, Inc - "Leveraging Desktop Audio and Video to Drive Your Organization's Corporate Communications"</li>

<li>Glen Martin, Director of Product Management, SpikeSource, Inc. - "An Overview of the Participatory Testing System"</li>

<li>Jono Bacon, Freelance Writer and Consultant - "Building the Next Generation Linux Desktop"</li>

<li>Si Chen, OpenSource Strategies - "Bringing Open Source to Enterprise Software: Opportunities and Challenge"</li>

<li>Kevin McElligott, iTech Developers - "phpWebSite: Open Source CMS"</li>
</ul>
</p>

