	<h3>2006 Southern California Linux Expo Call For Papers</h3>
	
	<p>
The USC, Simi/Conejo, and UCLA Linux User Groups are proud to announce
the <strong>4th</strong> annual Southern California Linux Expo scheduled for
<strong>February 11-12, 2006</strong> at the <strong>Radisson Hotel</strong>
near the Los Angeles International Airport. Building on the tremendous
success of last three years' SCALE, we will continue to promote Linux and
the Open Source Software community. 
</p>

<p>
We invite you to share your work on Linux and Open Source projects with
the rest of the community as well as exchange ideas with some of the
leading experts in these field. Details about SCALE 4X as well as
archives for the last three years can be found at
<a href="http://www.socallinuxexpo.com/">http://www.socallinuxexpo.com</a>.

<p>
<strong>All proposals are to be sent to kapadia=at=socallinuxexpo.com.</strong>
</p>

<p>
Topics of interest include, but are not limited to:
</p>

<ul>
<li>Linux kernel</li>
<li>Linux Networking</li>
<li>Linux for embedded systems</li>
<li>Linux for Desktops</li>
<li>LAMP</li>
<li>Multimedia in Linux</li>
<li>Security in Linux</li>
<li>VoIP</li>
<li>Wireless tools in Linux</li>
<li>Linux Games</li>
<li>GIMP &amp; other graphics software</li>
<li>Administration techniques for specific distributions</li>
<li>Custom Configurations</li>
<li>Linux Deployments and experiences: Case studies</li>
<li>Open source Licensing</li>
<li>Government policies with Open Source</li>
<li>Other open source projects</li>
</ul>

<p>
The proposals should comprise a 1-page (maximum) description containing
the following:
</p>

<ol>
<li>Title for the talk.</li>
<li>Name, Affiliation, Bio, a passport size picture (optional) and
contact email address of the Presenter.</li>
<li>What will be covered? A bulleted list of the main points of the
presentation will be ideal. Please include enough detail as will be
necessary.</li>
<li>Any specific requirements needed for the presentation other than an
overhead projector and a microphone.</li>
</ol>

<p>
Presentations are alloted a time slot of about 45 minutes.
</p>

