<h3>
Peter Quinn to Keynote OpenDocument Format in Government Workshop  at SCALE 4x
</h3>

<p>
<b>FOR IMMEDIATE RELEASE<br />
Los Angeles, CA<br />
January 18, 2006<br />
</b>
</p>

<p>
The Open Document Fellowship and the Southern California Linux Expo (SCALE) announced today that Peter Quinn, former CIO of Massachusetts, will keynote at the 2006 Southern California Linux Expo.  Quinn's keynote will be the opening presentation at SCALE's Workshop on the OpenDocument Format (ODF) in Government Organizations.
</p>

<p>
Date:  Feb 10, 2006<br>
Time: 1PM - 5PM<br>
Location: Radisson Hotel - Los Angeles Airport  (LAX)<br>
</p>

<p>
This keynote presentation will focus on the state of Massachusetts' transition to the OpenDocument Format (ODF).  Quinn will discuss the reasoning behind the decision to set ODF as a statewide standard.  He will provide an overview of his experience during this transition, including the technical and political pitfalls he encountered.  
</p>

<p>
Peter Quinn served as Chief Information Officer and Director of the Massachusetts Information Technology division for 4 years. (2002 - 2006).  As CIO, Quinn was responsible for setting information technology standards and policy for the state of Massachusetts.  Quinn is most well known for initiating Massachusetts' transition to OpenDocument Format.   Mr. Quinn came to public service following a successful career in private sector IT, most recently as the CIO for Boston Financial Data Services</p>

<p>
SCALE's workshop on OpenDocument format will take place on February 10, 2006.   The event will begin at 1 pm with Mr. Quinn's keynote presentation.  Other speakers in the workshop will include: </p>

<ul>
<li>- Douglas Heintzman (Director, Technical Strategy - IBM)</li>
<li>- J David Eiserberg (OpenDocument Fellowship)</li>
<li>- Gary Edwards (OpenDocument Fellowship)</li>
</ul>

<p>
Topics to be covered at this workshop include:
* The benefits of open standards versus their proprietary counterparts.
* The technical merits of OpenDocument and XML-based file formats.
* Avoiding vendor lock-in: The importance of choice in software for government organizations and their constituents.
* The security benefits of heterogeneous software environments.
<br><br>
Government representatives interested in attending should RSVP to odf@socallinuxexpo.org.  
</p>

<b><u>About the Southern California Linux Expo</u></b>

<p>
The Southern California Linux Expo is the premier grass roots Linux and Open Source Software conference in the United States.  SCALE 4x, their fourth annual event, will be held on February 10 - 12, 2006 at the Los Angeles Airport Radisson.  SCALE will include over 30 presentation and seminars by leaders from the open source community, tutorials for beginners, and an exhibit hall with both vendor booths and non-commercial booths.  Linux Expo of Southern California, Inc. is a non-profit organization currently comprised of, but not limited to, members of the USCLUG, UCLALUG, and SCLUG. For more information see http://www.socallinuxexpo.org
</p>
