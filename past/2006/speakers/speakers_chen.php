	<h3>Bringing Open Source to Enterprise Software: Opportunities and Challenges</h3>

	<p>
	At first glance, open source and enterprise software seem like polar opposites. Open source software is founded upon the ideals of free expression and open participation, and it is freely available to all potential users. On the other hand, enterprise software, such as Enterprise Resource Planning (ERP) and Customer Relationship Management (CRM), demand high reliability and predictability, and they traditionally have also been the most expensive of all software.<br /><br />

	Beneath the surface, however, open source software brings significant benefits to enterprises and business users by offering them greater flexibility, less vendor lock-in risk, and a lower cost of ownership. Does this mean that open source can succeed in the enterprise software segment? If so, what are the potential challenges ahead?<br /><br />

	Drawing upon our experiences as both developers and users of enterprise open source software, we would like to share some of the lessons we have learned in bringing open source to the enterprise. Topics of the presentation will include:

	<ul>
	<li>The Open Source Software Model</li>
	<li>The Enterprise Software Model</li>
	<li>How Enterprise Users Benefit from Open Source</li>
	<li>Why Open Source Needs to Succeed in the Enterprise Software Segment</li>
	<li>Bridging the Gap with Sequoia Open Source ERP: Our Experience</li>
	</ul>
	</p>

