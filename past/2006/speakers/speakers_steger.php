	<h3>Email: The Killer App for Linux</h3>

	<p>
	Linux-based email has so many advantages that it may well be the
	killer app for Linux, even serving to propagate Linux adoption in
	companies. In addition to the well-known reliability, scalability and
	TCO for which Linux is known, email on Linux offers easy co-existence
	in multi vendor environments, zero disruption for end users and
	freedom from vendor lock-in.  This session will explore these
	advantages, offering ammunition for Linux advocates to advance Linux
	email in their organizations as well as offer practical suggestions
	for migrating to Linux email.
	</p>

