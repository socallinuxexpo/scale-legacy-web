	<h3>Server Consolidation in Open Source Environments</h3>

	<p>
	Open source architecture is gaining stature with enterprise IT managers as a cost-effective alternative to proprietary operating systems.  Making open source even more attractive, multi-core processing can now help make the process of server virtualization a mainstream technology for open source infrastructures and promises to deliver more efficiencies and economies to IT departments. In this presentation, the speaker will describe recent advances in the areas of multi-core processing and virtualization for open source environments and how they can now help deliver greater performance from and efficient use of existing IT resources. He will explain how server virtualization enables a single machine to run multiple operating systems efficiently while maintaining secure, guaranteed isolation between systems. The session shows how server virtualization consolidates IT resources across the enterprise, reducing total cost of computing infrastructure, and allowing enterprises to run legacy operating systems and applications on top of new operating systems and hardware.<br /><br />
	 

	<b>What Attendees Will Learn:</b>
	<ul>
	<li>How to implement server virtualization in an open source environment to partition, consolidate and manage critical computing infrastructure.</li>
	<li>How server virtualization improves IT ROI and reduces total cost of ownership.</li>
	<li>Innovations in integrated memory controllers and transport technology that enable more efficient memory and I/O access in open systems.</li>
	<li>How multi-core processing along with server virtualization leverages open source flexibility for faster response to changing business demands.</li>
	</ul>
	</p>

