	<h3>Adam Leventhal</h3>

	<p>
	Adam Leventhal is a Solaris Kernel Engineer. He is one of the three
	authors of DTrace (the dynamic tracing facility new in Solaris 10), and
	is chiefly for the user-level tracing components. When he's not working
	on new features for DTrace, Adam has developed new observability tools
	for Solaris and spends a large amount of time applying those tools to
	bugs and performance problems. Adam joined the Solaris Kernel after
	graduating cum laude from Brown University in 2001 with a degree in
	Math and Computer Science.
	</p>

	<h3>"Performance Tuning Linux Applications With DTrace"</h3>

	<p>
	Despite the availability of faster and more sophisticated processors,
	developers still want to squeeze out every iota of performance from
	their applications. Due to growing complexity in applications and the
	underlying software and hardware stacks, finding bottlenecks in
	application performance is an increasingly difficult and daunting task.
	In November of 2003, Sun introduced DTrace, an open source framework
	that allows for observability at every level of the system from the
	lowest levels of the kernel to applications written in C/C++, Java, php
	and other languages. DTrace has been used on all kinds of applications
	with tremendous success, finding performance wins up to 3000% on
	business critical applications. Previously, DTrace's use had been
	limited to Solaris applications, but with the advent of the BrandZ
	Linux compatibility environment developers can examine their Linux
	applications using DTrace to find bugs and optimize performance. This
	session will describe how to use DTrace on Linux applications, and will
	include concrete examples of how DTrace has been used to improve
	performance. 
	</p>

