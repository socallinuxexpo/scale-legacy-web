	<h3>Linux And Virtualization</h3>

	<p>
	Linux in the enterprise is a core component of true .next-generation. architectures. But tomorrow.s companies will use more than just open source code for cost and performance benefits. In fact, the mandate to reduce costs within the IT department without sacrificing performance is generating renewed buzz around virtualization. Linux server virtualization has been at the forefront of the virtualization movement and together Linux and virtualization are the foundation for the future of enterprise computing. However, even with the increased enterprise move to open source, proprietary virtualization solutions still lead the market. This session will look at the advantages to both proprietary and open source virtualization solutions; provide real-world examples of how enterprises are augmenting the ROI they achieve with Linux through server virtualization; and discuss how Linux and virtualization are enabling "next-generation" enterprise computing solutions.<br /><br />

	Attendees Will Learn:
	<ul>
	<li>Overview of virtualization in a Linux environment</li>
	<li>Open source vs. proprietary virtualization solutions</li>
	<li>How virtualization improves ROI in Linux environments</li>
	</ul>
	</p>

