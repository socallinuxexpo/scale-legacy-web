	<h3>Linux Network Security</h3>

	<p>
	A Network Intrusion Detection Systems (NIDS) is a system that helps to thwart various malicious activities. Various types of intrusion detection systems and firewalls have become prolific in the open source community. However, they.re often not implemented to their fullest potential due to their time consuming nature. They are allowed to become outdated and the logs are often not reviewed on a regular basis. Even when these tasks can be automated, the systems are often left ineffectual and can go from being a security system to becoming a security hole.<br /><br />

	In this presentation we will cover the tools used to defend networks and the flaws in most of their implementations. Once we have explained the tools we will go into further detail on using these tools to maximize protection. We will cover things that can be done on the network edge and how to automate the process of keeping the new services updated across the Enterprise.<br /><br />

	In the spirit of fostering a stronger open source community, this presentation will focus on open source tools. We will cover where to get the tools, managing their deployment and the options available to administrators. Finally we will cover the limitations of Network Intrusion Detection Systems and the ways that these systems can be turned against their administrators.<br /><br />
	</p>

