	<h3>"Linux and CinePaint: OSS in Hollywood"</h3>

	<p>
	Linux is the #1 operating system in Hollywood feature film animation and
	visual effects, for both servers and desktops. Why are Disney,
	DreamWorks, ILM, Pixar, and Sony running on Linux? Note that the
	studios' filmmaker tools running on Linux are proprietary -- except one.
	CinePaint is used to retouch films such as Harry Potter and Spider-Man.
	It was used to add flying arrows in The Last Samurai. For 2006 CinePaint
	has a totally new architecture based on FLTK, a fast free portable GUI
	API contributed by the film industry. What's new in CinePaint and what
	does it mean for users outside the film industry?
