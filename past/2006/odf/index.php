<b>About The OpenDocument Format (ODF):</b><br /><br />
<p>
OpenDocument (ODF) is a file format developed by OASIS, a standards body focused on structured documents and XML.  ODF is intended as an open, cross platform, and vendor independent standard for editable office documents including:  text documents, spreadsheets, charts, graphical
documents, and databases.  The format competes with Microsoft's Office 2003 and Office 12 XML file formats. </p>
<p>
ODF is supported by a wide range of software companies, government organizations, and open-source projects including:  Sun Microsystems, IBM, Novell, Adobe, Corel, The National Archives of Australia, and others.  Applications which currently support ODF include: OpenOffice.org, KOffice, Sun's StarOffice, and IBM Workplace. The royalty free and open license under which ODF is released will allow any individual or organization to develop competing products based on this format, without restrictions.  This openness prevents vendor lock in, increases marketplace competition and provides consumers with a wide variety of choices in office suites.</p>

